                            <h2>Plateforme Audiotel © : Ergonomie et Performances</h2>
    <p>Afin de faciliter la tâche de ceux qui fournissent des services téléphoniques, on peut leur proposer une <strong>plateforme audiotel</strong> qui pourra jouer le rôle d'un véritable serveur vocal interactif prenant en charge les appels entrants. Ainsi, les personnes pourront laisser des messages vocaux enregistrés et elles auront aussi la possibilité d'nteragir avec le serveur au moyen des touches de leur téléphone ou en répondant oralement à une question posée.</p>
<h3>Fonctionnement de la plateforme Audiotel</h3>
<p>Le système de la <strong>plateforme audiotel</strong> fonctionne selon une logique préprogrammée en diffusant des messages préalablement enregistrés ou en permettant l'accès à des bases de données. L'un de ses principaux avantages est sa capacité à traiter de nombreux appels qui arrivent simultanément. Ainsi, la plateforme utilise une technologie de reconnaissance vocale qui facilite la tâche de l'appelant en lui permettant de dialoguer avec le serveur en prononçant des mots simples. En outre, son système de reconnaissance vocale lui permet d'interpréter les réponses données par l'appelant à des questions ouvertes.</p>
<p>Le système de la <strong>plateforme audiotel</strong> a par ailleurs la capacité de reconnaître le numéro de téléphone d'un appelant et de prendre en compte l'heure de son appel ainsi que la durée de son attente. D'autre part, la logique suivie par le serveur vocal est souvent constituée par un arbre de décision qui permet d'atteindre le résultat souhaité.</p>
    <h3>Traitement des appels par la plateforme Audiotel</h3>
<p>En conséquence, la <strong>plateforme audiotel</strong> facilite le traitement des appels de personnes ayant des besoins pouvant être très différents et parfois compliqués. Par ailleurs, des méthodes de pointe telles que l'intelligence artificielle ou les règles régissant la conception des algorithmes peuvent être utilisées pour l'utilisation d'une plateforme audiotel. Il en résulte une très grande simplification de la gestion des appels et des gains de productivité très importants.</p>
<p>En ce qui concerne les sorties du serveur vocal, elles se font sous forme d'un message préenregistré dans un fichier audio ou sur une bande magnétique. Il est notamment possible d'utiliser la synthèse vocale pour modifier le contenu de paragraphes simples prononcés oralement tels que des valeurs chiffrées. On peut aussi intégrer le serveur vocal à un centre d'appel, ce qui permet de transférer les appels entrants à l'opérateur. On peut par ailleurs le faire fonctionner avec un système permettant l'envoi de SMS ou d'e-mails.</p>

                       	<h2>Numéro d’astreinte téléphonique</h2>
   <p> Gérer la logistique des <strong>astreintes téléphoniques</strong> (services de garde, hotline, assistance…) nécessite un investissement conséquent, en particulier, pour la permanence téléphonique 24h/24.<br />
Média Technologies vous propose de faire des économies et de vous faciliter la gestion de l’<strong>astreinte téléphonique</strong>, par la mise en place d’un numéro unique qui redirigera les appels vers le ou les salariés d’<strong>astreinte</strong>.
 </p>
    <h3>Comment fonctionne l'astreinte téléphonique ? </h3>
   <ul>
   <li>Nous vous donnons accès, sur un extranet, à votre planning d’astreinte que vous saisissez et modifiez facilement.</li>
   <li>Vous pouvez planifier plusieurs personnes sur la même tranche horaire d’astreinte.</li>
   <li>Les personnes, qui composent le numéro unique d’astreinte, seront automatiquement redirigées vers le personnel d’astreinte.</li>
	</ul>
   
	<h3>Les avantages de l'astreinte téléphonique</h3>
<ul>
   <li>Vous gérez uniquement le planning d’astreinte, la redirection des appels vers les salariés d’<strong>astreinte</strong> est automatisée.
   <li>Vous pouvez consulter, en temps réel, quel salarié est parti en intervention.
   <li>Vous avez accès aux statistiques détaillées vous permettant d’améliorer votre <strong>service d’astreinte</strong>, et ainsi de satisfaire l’appelant.
   <li>Vous êtes alerté si la personne d'astreinte ne prend pas les appels.
   <li>Nous adaptons l’<strong>astreinte téléphonique</strong> en fonction de vos besoins.
</ul>


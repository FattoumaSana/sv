                       	<h2>Le pointage téléphonique - Surveillez vos salariés</h2>
   <p>La journée de travail de vos salariés (aides à domicile, personnel de nettoyage, dépanneurs, démonstrateurs…) s’effectue à l’extérieur et sur des sites différents.
Le <strong>pointage téléphonique</strong> est la solution, vous permettant de savoir en temps réel si votre salarié est sur le lieu de sa mission.</p>
    <h3>Comment fonctionne le pointage téléphonique ? </h3>
<p>Votre salarié appelle notre serveur vocal, en arrivant et en partant du site.<br />
<p>Un message d’accueil lui demande de saisir son code d’accès à (X) chiffres puis :<br />
- S’il arrive, de « taper 1 »<br />
- S’il part, de « taper 2 »<br />
Il pourra alors raccrocher.
</p>
<p>Le <strong>serveur vocal</strong> enregistre immédiatement les données (numéro d’appel, le code du salarié, la date et l’heure de l’appel).
</p>
	<h3>Avantages du pointage téléphonique </h3>
<ul>
<li>Toutes ces informations seront consultables en temps réel et exploitables</li>
<li>Vos clients seront rassurés sur la prestation de vos salariés.</li>
<li>Vous disposerez de données complémentaires pour établir les fiches de paie.</li>
<li>Nous pouvons déclencher un processus éventuel d’alerte en cas d’absence de votre salarié, vous recevrez un message sur votre portable et/ou votre email.</li>
<li>Nous adaptons le <strong>pointage téléphonique</strong> en fonction de vos exigences.</li>
</ul>

    <h2>Conf call - La conférence téléphonique</h2>
 <p>Une conférence téléphonique (<strong>conf call</strong>), notamment de réunions de travail aux formations à caractère téléphonique, est une conférence qui se pratique à distance et à travers un réseau de télécommunication. Une <strong>conf call</strong> permet à deux ou plusieurs personnes d’établir une discussion de manière collective sur une même ligne téléphonique. Le principe se base sur l’obtention d’un numéro dédié, la gestion de conférence et la sécurisation avec un code.</p>
	
	<h3>Déroulement d'une Conf Call</h3>
	<p>Une <strong>conf call</strong> réussie se repose sur l’animation, la ponctualité des participants, la qualité supérieure du son et l’organisation selon l’ordre du jour. Un organisateur parmi les intervenants s’occupe préalablement du plan de réunion à respecter durant la réunion. Le rôle d’un animateur consiste à présenter les participants en procédant par l’identification et la reconnaissance vocale de chaque intervenant et à diriger la conférence, afin de dissiper des éventuelles confusions pouvant affecter la compréhension et la mémorisation des points discutés.</p>
    
    <h3>Les avantages d'une Conf Call</h3>
<p>Une <strong>conf call</strong> a été conçue pour permettre une communication facile et pour des échanges d’idées sur la même ligne, entre plusieurs intervenants disposant d’un appareil téléphonique. Cette discussion en ligne réduit largement le temps et le coût financier correspondant aux déplacements. La seule lacune concerne la délicatesse et la difficulté d’évaluer le ton de chaque participant, pouvant ainsi aboutir à de mauvaises interprétations compte tenu de l’absence d’expression faciale. Dès que tous les intervenants disposent d’un téléphone, une liaison <strong>conf call</strong> peut être établie immédiatement et ne nécessite aucune connexion à un ordinateur ou à l’internet. Ne dépendant pas du nombre de participants ni d’aucune durée limite, le service de <em>conférence téléphonique</em> peut donc être pratiquement accessible à tout moment.</p>


<h3>Effectuer une Conf Call sécurisée</h3>
<p>Dans un contexte de sécurité, un code d’accès est communiqué préalablement à chaque participant pour l’intégration en ligne pour entamer une réunion téléphonique. En cas de besoin, il est possible d’enregistrer le déroulement d’une <strong>conf call</strong> via un serveur en vue d’un téléchargement ultérieur. </p>

                       	<h2>Plateforme</h2>
    <p>Cela fait déjà plusieurs années que nous gérons la partie téléphonie de plateformes web<br/>
      Nous avons acquis un savoir-faire et maitrisons des niveaux de sécurité qui nous permettent aujourd'hui d'interfacer (proposer le service téléphonique) pratiquement n'importe quelle plateforme du marché.<br />
      Le principe de fonctionnement de ces plateformes est semblable à celui des sites e-commerces. La différence est que le client n'achète pas un objet mais achète du temps de communication.<br/>
	  Une fois son crédit temps acheté, le client compose un numéro de téléphone (généralement un numéro géographique local), choisit le conseiller avec lequel il veut être mis en relation en tapant son code sur le clavier de son téléphone.<br/>
	  Ces plateformes téléphoniques sont utilisées pour deux principales raisons. La première raison est de rémunérer un service pour une mise en relation avec un conseiller spécialisé dans tel ou tel domaine (ex: <a href="https://www.serveur-vocal-audiotel.fr/plateforme-voyance.php" title="Plateforme de voyance">plateforme voyance</a>). La deuxième raison est de donner un accès privilégié à sa clientèle en leur permettant d'accéder directement à leur interlocuteur. Dans ce cas le conseiller n'est pas "pollué" par des appels venant de n'importe où. Le client actif sur la plateforme est filtré par son code client et un temps de communication déterminé par l'entreprise valable pour une durée déterminée. C'est par exemple le cas pour des hotline à abonnement. 
    </p>

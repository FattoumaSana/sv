                            <h2>Serveur Audiotel : Une solution populaire</h2>
    <p>D'abord utilisé pour des services de jeux et des services de voyance, le <strong>serveur audiotel</strong> est désormais présent quand on appelle les administrations, la SNCF et ou encore les cinémas. Il est également identifiable par le numéro de téléphone qui est soit court (3949 pour pôle emploi), soit débute par 08 9x. Fini la standardiste qui transmet les appels: vous êtes désormais accueilli par un serveur informatique dont les répliques aimables sont pré-enregistrées.</p>
    <h3>Les avantages d'un serveur Audiotel</h3>
<p>Le <strong>serveur audiotel</strong> avantage aussi bien l'entreprise que l'interlocuteur. Pour l'entreprise, le <strong>serveur audiotel</strong> permet de traiter davantage d'appels et d'orienter les gens vers les bons services, voire les bons téléconseillers. Certaines entreprises mettent ce service payant, ce qui leur permet de générer un peu de revenus sur l'information clientèle ou l'après-vente.</p>
<p>Du côté de l'usager, il s'agit aussi d'un gain de temps car son appel est pris rapidement en considération et si toutefois le serveur ne lui trouve pas un interlocuteur adéquat, l'usager en est également informé et ne reste pas en ligne indéfiniment. Enfin,comme abordé dans l'introduction, le <strong>serveur audiotel</strong> est un hôte parfait et aimable, programmé pour ne pas s'impatienter et bien accueillir la personne à l'autre bout du fil.</p>
    <h3>Les limites d'un serveur Audiotel</h3>
    <p>S'il semble accomplir des performances, le <strong>serveur audiotel</strong> a certaines limites. C'est le cas notamment du <strong>serveur</strong> à reconnaissance vocale où il faut s'évertuer à bien articuler pour qu'il comprenne le service souhaité. Celui-ci a dû d'ailleurs se faire appeler par tous les noms d'oiseaux par les interlocuteurs impatients qui n'aiment déjà pas son collègue, le répondeur.

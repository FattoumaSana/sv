	<h2 class="h1">Conférence téléphonique - Vos réunions à distance</h2>
    <p>La <strong>conférence téléphonique</strong> est le moyen le plus simple, rapide et économique pour permettre aux personnes partout dans le monde de se réunir téléphoniquement, en toute autonomie.</p>
    
   
<p>En effet, la <strong>conférence téléphonique</strong> peut être activée à n’importe quel moment, depuis n’importe quel téléphone (fixe ou mobile), en communicant en toute simplicité aux participants le numéro à appeler et le code d’accès à la salle virtuelle. 
</p>
    
    <h3>Préparer une conférence téléphonique</h3>
    <p>L’Organisateur (ou modérateur) invite les participants en leur indiquant la date et l’heure de la <strong>conférence téléphonique</strong> (<a href="https://www.serveur-vocal-audiotel.fr/conf-call.php" title="Conf Call">Conf Call</a>), le numéro de téléphone à appeler et le code d’accès à composer. Quelques minutes avant le début de la réunion, l’Organisateur appelle le numéro de téléphone et en suivant les instructions, il compose le ou les codes d’accès, afin d’activer la <strong>conférence téléphonique</strong>.</p>

	<h3>Déroulement d'une conférence téléphonique</h3>
<p>A l’heure indiquée dans l’invitation envoyée, les participants composent le numéro de téléphone ainsi que le code d’accès. La <strong>conférence téléphonique</strong> peut ainsi commencer !</p>
<ul>
<li><u>ECONOMIQUE</u> : La conférence téléphonique réduit les coûts liées aux déplacements et aux voyages, ainsi que le temps dédié à l’organisation des réunions</li>
<li><u>EFFICACE</u> : elle favorise la fréquence des relations entre les collègues, les collaborateurs d’une entreprise et leurs clients ; simplifie les processus et il est tout à fait possible de l’enregistrer pour la réécouter et la transmettre par mail aux participants.</li>
<li><u>SIMPLE</u> : elle peut être activée en quelques secondes, depuis un téléphone fixe ou mobile ; une voix enregistrée dans la langue choisie indique aux participants les différentes phases d’accès pour la connexion ; le service est disponible 24h/24, sans réservation préalable ; toutes les opérations peuvent être effectuées depuis le clavier du téléphone.</li>
<li><u>SECURITE</u>: la confidentialité de la <strong>conférence téléphonique</strong> est garantie par un mot de passe numérique que l’Organisateur peut changer chaque fois que nécessaire ; l’Organisateur peut « fermer » la salle de réunion virtuelle une fois tous les participants déconnectés.</li>
</ul>

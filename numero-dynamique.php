   <li>Installation du <strong>numéro dynamique</strong> est simple et rapide.</li>
   <li>Mise en place et hébergement gratuits.</li>
   <li>La qualité des appels est équivalente à un appel classique parce que ce n’est pas une voix IP (via internet).</li>
